    class Solution {
        public String longestPalindrome(String s) {
            int n = s.length();
            if (n < 2) return s;

            int maxLength = 1;
            int startIndex = 0;
            int endIndex = 1;

            boolean[][] dp = new boolean[n][n];

            for (int r = 1; r < n ; r++) {
                for (int l = 0; l < r; l++) {
                    if (s.charAt(l) == s.charAt(r) && (r - l <= 2 || dp[l + 1][r - 1])) {
                        dp[l][r] = true;

                        if (r - l + 1 > maxLength) {
                            maxLength = r - l + 1;
                            startIndex = l;
                            endIndex = r + 1;
                        }
                    }

                }

            }

            return s.substring(startIndex, endIndex);

        }
    }
