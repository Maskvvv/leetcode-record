    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        List<String> res = new ArrayList<>();


        public List<String> generateParenthesis(int n) {
            dfs(n, 0, 0, "");
            return res;
        }

        public void dfs(int n, int l, int r, String path) {
            if (l > n || r > l) return;

            if (l + r == 2 * n) {
                res.add(path);
            }

            dfs(n, l + 1, r, path + "(");
            dfs(n, l, r + 1, path + ")");
        }
    }
